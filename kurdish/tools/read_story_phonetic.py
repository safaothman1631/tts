from kurdish_pronunciation import speak_kurdish_phonetic

print("=" * 70)
print("🎤 خوێندنەوەی چیرۆکی 'ڕوونی لە ناو تاریکییەوە'")
print("   Reading 'Light Within Darkness' with Kurdish pronunciation")
print("=" * 70)

# چیرۆکی تەواو / Full story
story = """
لە گوندێکی بچووک لە کوردستان، کوڕێک هەبوو بە ناوی ئارمانج. 
ئارمانج گەنجێکی مەهربان بوو، بەڵام دەترسا لە تاریکی. 
دایکی پێی گوت: تاریکی خۆی ترسناک نییە، تۆ ترسی دەهێنیت. 
ڕوونی لە ناوخۆت دروست بکە، دڵەت خۆی بێ تاریکی. 
ئارمانج قسەکەی دایکی لە دڵ خست، بەڵام ترسەکە هەر لە ناوخۆی مانابوو. 
شەوێک، ئارمانج داپرسی: دایی من هەر دەترسم، چۆن ڕوونی دروست بکەم؟ 
دایک گوتی: کوڕم، هەر کاتێک بڵێی من دڵ قەویەم، ڕوونی دروست دەکات. 
ئارمانج فێربوو. شەوان دەیگوت: من دڵ قەویەم، من ڕووناکم، من تەنیا نیم. 
لە کۆتاییدا، ئارمانج زاڵ بوو لەسەر ترسەکەی و چیتر نەترسا.
"""

print("\n📖 چیرۆکی کوردی:\n")
print(story)

print("\n🔄 گۆڕین بۆ پرۆنانسیەیشن و خوێندنەوە...\n")

# خوێندنەوە / Read it
speak_kurdish_phonetic(story, "output/story_kurdish_pronunciation.mp3")

print("\n✅ تەواو! فایل هەڵگیرا: output/story_kurdish_pronunciation.mp3")
print("💡 ئێستا دەنگی ئینگلیزی وشەکانی کوردی دەخوێنێتەوە!")
